#include "3-calc.h"

/**
 * op_add - calculatess the sum of a and b.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculatess the difference of a and b.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of a and b.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of a by b.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the remainder of the division of a by b.
 * @a: the first number.
 * @b: the second number.
 *
 * Return: remainder of a divided by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
